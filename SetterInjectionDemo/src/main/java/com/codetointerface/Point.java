package com.codetointerface;

public class Point {
	private int pointX;
	private int pointY;

	/**
	 * @return the pointX
	 */
	public int getPointX() {
		return pointX;
	}

	/**
	 * @param pointX the pointX to set
	 */
	public void setPointX(int pointX) {
		this.pointX = pointX;
	}

	/**
	 * @return the pointY
	 */
	public int getPointY() {
		return pointY;
	}

	/**
	 * @param pointY the pointY to set
	 */
	public void setPointY(int pointY) {
		this.pointY = pointY;
	}

	

}
